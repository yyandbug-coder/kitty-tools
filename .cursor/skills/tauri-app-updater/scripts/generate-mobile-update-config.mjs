#!/usr/bin/env node
/**
 * 根据 release.config.json 生成移动端更新配置（供前端跳转 Release 页）。
 */
import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

import { loadReleaseConfigRaw } from './lib/load-release-config.mjs'
import { buildMobileUpdateConfig } from './lib/release-targets.mjs'
import { getProjectRoot } from './lib/skill-paths.mjs'

const projectRoot = getProjectRoot()
const releaseConfigRaw = loadReleaseConfigRaw(projectRoot)
const config = buildMobileUpdateConfig(releaseConfigRaw)

const args = process.argv.slice(2)
function readArg(name) {
  const index = args.indexOf(name)
  if (index === -1 || index === args.length - 1) return ''
  return args[index + 1]
}

const outPath =
  readArg('--out') || join(projectRoot, 'src/lib/mobile-update-config.generated.ts')

const content = `// AUTO-GENERATED by tauri-app-updater — do not edit manually.
// Regenerate: pnpm release:mobile-config
// Source: release.config.json → mobile.update

export const mobileUpdateConfig = {
  target: '${config.target}',
  versionSource: '${config.versionSource}',
  versionCheckUrl: ${JSON.stringify(config.versionCheckUrl)},
  releaseApiUrl: ${JSON.stringify(config.releaseApiUrl)},
  releasePageUrl: ${JSON.stringify(config.releasePageUrl)},
  releasePageTagBase: ${JSON.stringify(config.releasePageTagBase)},
} as const

export type MobileUpdateConfig = typeof mobileUpdateConfig
`

mkdirSync(join(outPath, '..'), { recursive: true })
writeFileSync(outPath, content, 'utf8')
console.log(`[generate-mobile-update-config] 已写入 ${outPath}`)
